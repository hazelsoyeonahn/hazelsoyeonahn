package lectureCode;

public class Remainders {
	/**
	*
	* This class demonstrates the use of the modulus operator
	* given the values 150, 10, 100, 200
	*
	* The modulus operator returns the remainder after division.
	*
	* */
	public static void main(String[] args){
		int value = 150;
		System.out .println("Value = "+value);
		System.out .println("Value mod 100 = "+(value % 100)+"\n");
		value = 10;
		System.out .println("Value = "+value);
		System.out .println("Value mod 100 = "+(value % 100)+"\n");
		value = 100;
		System.out .println("Value = "+value);
		System.out .println("Value mod 100 = "+(value % 100)+"\n");
		value = 200;
		System.out .println("Value = "+value);
		System.out .println("Value mod 100 = "+(value % 100)+"\n");
	}
}
